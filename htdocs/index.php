      <h3 id="about">About</h3>

      <p><acronym>JabRef</acronym> is an open source
      bibliography reference manager. The native file format used
      by <acronym><span class="red">J</span><span class=
      "orange">ab</span>Ref</acronym> is <acronym>BibTeX</acronym>,
      the standard <acronym>LaTeX</acronym> bibliography format.
      <acronym><span class="red">J</span><span class=
      "orange">ab</span>Ref</acronym> runs on the Java VM (version
      1.4.2 or greater), and should work equally well on Windows,
      Linux and Mac OS X.</p>

      <p>BibTeX is an application and a bibliography file format
      written by Oren Patashnik and Leslie Lamport for the LaTeX
      document preparation system. <a href=
      "http://www.ecst.csuchico.edu/~jacobsd/bib/formats/bibtex.html">
      General information about BibTeX</a>.</p>

      <p>Bibliographies generated by LaTeX and BibTeX from a BibTeX
      file can be formatted to suit any reference list
      specifications through the use of different BibTeX style
      files. We support this initiative to build a searchable
      database of BibTeX style files, organized by journal names:
      <a href="http://jo.irisson.free.fr/bstdatabase/">LaTeX
      bibliography style database</a>.</p>

